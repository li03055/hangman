def func(num1, num2):
    """
    Returns num1 divided by num2
    Parameters:
    num1 (int): The first number to divide
    num2 (int): The second number to be added
    """
    return num1 / num2

def main():
    result = func(3, 4)
    print(result)

if __name__ == "__main__":
    main()
